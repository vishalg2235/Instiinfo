<br><br><b>1-Proper mentor </b>is must necessary to guide you how to prepare for exams, which books you should refer and guide you whenever you need it. I would like to mention my mentor from SWG(Prasoon Bopche) which helped me a lot. Apart from that one can always have funda from seniors.
