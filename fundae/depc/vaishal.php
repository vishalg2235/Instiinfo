<br><b>3-Smart work.</b> Solve tutorials that are taught in the class. If you can solve tutorials and have clear concepts, you need not solve whole reference book of that topic..Reference books should be solved in free time once you are finished with tutorials. Even you can get Ex if you start preparing a week before endsems, but you should attend classes regularly to have clarity of concepts and tutorials solved regularly.
<br><b>4-Don't waste much time for labs.</b> Many times, it depends on your luck and your impression on teacher :) 2-3 days of study for lab tests is sufficient. Most of the marks in labs depend on regular classes, so try to perform well in the classes.
<br>5-Last one, <b>don't take too much load</b> study freely and regularly.Enjoy your 1st year as well as manage your studies!!
