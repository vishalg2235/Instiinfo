			Hi, <br/>
  <p>Hola Freshers. Congratulations on landing in one of the most sought after institutes of this country.
One year ago at around the same time, I was attending the Orientation Programme. The Dean of Student Affairs (DoSA) was
speaking about the inevitable aspects of college life and then he said, "Now I am going to speak about the thing that 
you all have been eagerly waiting. Department Change." That's when I knew that this was a sort of craze amongst everyone,
just like getting into IIT was. DoSA spoke as frankly as he could. Most of the students come here after making a 
committment to themselves, full of determination. But, Getting up at 5:30 in the morning one day and 9:30 the next day,
is not determination. Rather, getting up at 7 everyday promptly is.</p>

<p>So if you are really determined, this piece is for you. First of all, get acquainted with the exact rules of department change.
The UG Rules and Regulations file, which might have been mailed to you contains all of it. Many students remain in doubt 
regarding that. Clarify yourself.</p>

<p>The main thing that one must keep in mind is that department change and securing a good cg is NOT an unsurmountable job.
The only thing that is expected is sincerity, self discipline, persistence, consistent determination and belief in self.
Studies are not at all tough. My personal experience is that if you do even a fraction of what you did for JEE Advanced, 
you can get a cg more than 9.0.</p>

<p>If you aim for depC, that doesn't mean you can't ivolve in other activities. If you are an excellent time manager, then
you can join any society you like and successfully manage your studies too. Keep yourself regularly up to date and your 
concepts crystal clear. Regularly attending all the class despite how sleepy you feel reflects your determination.
I really felt that the things that you learn in class from the good profs, you no longer need to study them from any other
book. Just practice a few questions and you are done. Sometimes you may find some profs no up to your expectations. Their
teaching may bore you and you may not understand what is being taught. Such things happen with everybody. But still,
attend their classes. Try to understand. Keep pushing and don't settle.</p>

<p>More than often, you will feel demoralized on seeing others studying a lot, being able to solve a problem with the flick of an
eyelid with which you have been struggling. That's the point when faith in yourself will help you. Don't worry. Your
peers are as much intelligent here, if not more. But the difference in result reflects sincerity and hard work, not
intelligence. So compensate your intelligence with hard work. Study smartly. Don't study everything. Study only what is
being taught in class. If you find a topic immensely intersting and want to study more on it, certainly do it. But not at
the expense of not paying attention to other subjects. Maintain a balance between all the subjects. If you don't like any
subject, still study it mechanically, pretend as if you are having fun. You might even have a little fun by accident!</p>

<p>And sometimes it happens that a student does not score good in the first semester and gives up hope after seeing the last year's 
depC statistics. DON'T YOU DARE DO IT. The cutoffs for any department do not follow any particular trend and vary largely
every year. So don't give up hope. The contrary is also not an exception. Often, people score an awesome cg in the first
sem too and cannot do so in the second sem. The reasons for this are purely personal and only you can prevent it from
happening. Refrain from it.</p>

<p>Needless to say, there a 'n' number of distractions. After all its college life, what do you expect? Self discipline will help
in such moments. Form your ideals and abide by them. Work hard. You can always enjoy after completing your work. Another
thing, don't try to study everytime. Keep a fixed number of hours and complete your study within that. Remaining time, do
what interests you. Watch tv series, movies, socialize, learn something new, read books anything. Obsession is good but 
overindulgence is a dacoit.</p>

<p>Once again, read the file UG Rules and Regulations. About 10% students getting out or coming into a dept. and all those rules.
Best of luck to all of you. Enjoy your first year experience. Trust me, KGP has to offer many new things to learn apart from
academics.</p>

Rajarshi Khan  
			
			
			 
				
