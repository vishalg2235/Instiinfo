I am pretty sure that there are quite a few of you, who are planning on for a Department Change by mugging up very hard starting right from this semester. So as a senior of yours, I would love to give you some tips and tricks to get things done!</p>

Priorities and Proactivity!<br/>
 <p>First of all, You need to get your priorities right. It could be as simple as choosing between bathing and using Facebook and as complicated as choosing between studying and societal activities. Keep it simple and do important things before you feel that you are going to open Facebook. :P (As it is a symbol of you having free time to pass and you are going to spoil it by scrolling on Facebook). In the meantime also keep it flexible so as to make sure you are missing out on something, for example: Suppose there is an Info Seminar of a society right after your classes, Go for it first and then make sure that you are studying that days stuff at night. Believe me if you study for half an hour everyday about what was taught in the class, you'll get through! It might look simple but as the time passes, you'll know! But then if you really want to do something, you'll find time for it, No matter what.</p> 
<p>After a month or so you might be in some society or the other and you might feel that it is taking up a lot of time and think of quitting it but take this a word from me: " Leaving societies won't help your acads, not wasting your time on stupid things and doing everything which you like while managing time will surely do. After all if you don't do stuffs which you like now then when?)?</p>
<p>
Time Management!<br/>
DO NOT let that half an hour pile up and make 3-4 hours as I am pretty sure if you really want to explore this new awesome home of yours, you won't really like giving those hours to study. Remember inculcating a good habit is not easy but once you are through the inculcation part, the other side is beautiful and rewarding. So keep it regular.</p> 
<p>
Attending Classes!<br/>
Believe me courses are not that tough after all you all have cleared JEE and reached here so if you know what has been taught you'll find the exams pretty easy. But there comes the big problem, DO NOT expect spoon feeding as in your coaching institute in Kgp. You'll find few professors very boring and  but that shouldn't make you stop attending their classes(Which is the general trend in Kgp, in a class 250 students you someday might find only 30-40 students!). But attending classes has its own benefits, the most important thing it does is tell you about the real syllabus, it might sound awkward but yeah you'll find your friends running asking "Yaar physics ka portion kya hai". It keeps you connected to the class and helps maintain your tempo towards that subject and if you're extrovert enough you can kill the professors with lot of questions which will force him to teach better. Sometimes you might find professors some important questions which will surely help you in the exams. This will happen the most in PHYSICS and CHEMISTRY subjects. </p>
<p>
Curiosity!<br/>
I am sure that most of you enjoyed studying in 11th and 12th. Well that could be because of your teachers then but now you need to develop curiosity of asking about everything and anything to anyone, the better you'll understand something the more you'll like it. Make contacts with seniors as many as possible they have been in your position and they know what is wrong and what is right.</p>

<p>
Tricks!<br/>
1. In the beginning days of your classes you'll get to know about the most sincere students of your section sitting in the first benches, make good friendship with them, no matter what, they will be mostly clear about most of the topics. So if you face any kind of problem, you can always get to them and get clarified. This will help both of you.
?<br/>
2. In labs you need to be very active and communicate as much as possible your TAs, make them remember your face, it does help and also clarifies your concepts.
?<br/>
Some subject wise tips would be:<br/>
1.Physics- Go to classes, know the syllabus properly and read from Saraswat and solve all the questions from it, it'll definitely help you the most.<br/><br/>

2.Mechanics- Concentrate in the class and work the tutorial question completely(Enough for A or EX). Use books only sometime for clarifying some tough topics.<br/><br/>

3.PDS- Well you need to concentrate on the labs, it'll help you a lot. If you are new to Coding it'll be tough in the beginning but Don't leave it like that, work hard and you'll get it. ANSI-C is a great book for basics.<br/><br/>

4.Electrical- Same as mechanics but grades are tougher to score! Also you need to be thorough with the concepts, if not electrical lab will kill you, Professors of electrical are very serious about the course and will ask very conceptual questions in the VIVA.  <br/><br/> 

5.Maths-1- This will turn out to be very easy if you practice well, class notes will be of the best help.<br/><br/>

6.Chemistry- Class notes and presentation shown in the class which can be found online will be really helpful. Easy scoring subject, you need to use it well.<br/><br/>

7.English- I have no idea how grading is done so ask someone else. :p<br/><br/>
